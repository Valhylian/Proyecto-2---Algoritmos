    private void cargarImagen(int generacion) throws IOException{
        String nombreImagen = "Generacion"+generacion;
        Path root = Paths.get(".").normalize().toAbsolutePath(); 
        String ruta = root.toString()+"\\src\\imagenes\\"+nombreImagen+".png";
        File archivoImagen = new File(ruta);
        BufferedImage imgenReferencia = ImageIO.read(archivoImagen); //cargo imagen de laberinto inicial
        ImageIcon imagen = new ImageIcon(imgenReferencia);
        jLabel1.setIcon(imagen);
        
        String ruta2 = root.toString()+"\\src\\imagenes\\"+nombreImagen+".txt";
        Path fileName = Path.of(ruta2);
        String content = Files.readString(fileName, StandardCharsets.US_ASCII);
        jTextArea2.setText(content);

    }
